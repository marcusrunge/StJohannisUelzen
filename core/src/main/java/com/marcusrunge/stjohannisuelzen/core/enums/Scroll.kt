package com.marcusrunge.stjohannisuelzen.core.enums

/**
 * Represents the direction of a scroll gesture.
 *
 * This enum is used to specify whether a scroll event is in the upward or downward direction.
 */
enum class Scroll {
    /**
     * Indicates a scroll in the upward direction.
     */
    Up,

    /**
     * Indicates a scroll in the downward direction.
     */
    Down,
}
