package com.marcusrunge.stjohannisuelzen.core.enums

/**
 * Represents the direction of a swipe gesture.
 *
 * This enum is used to specify whether a swipe event is to the left or to the right.
 */
enum class Swipe {
    /**
     * Indicates a swipe to the left.
     */
    Left,

    /**
     * Indicates a swipe to the right.
     */
    Right
}
